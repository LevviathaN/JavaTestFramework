package utils.bpp_old;

public class ElementNotClickableException extends Exception {

    public ElementNotClickableException() {
        super();
    }

    public ElementNotClickableException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public ElementNotClickableException(String message, Throwable cause) {
        super(message, cause);
    }

    public ElementNotClickableException(String message) {
        super(message);
    }

    public ElementNotClickableException(Throwable cause) {
        super(cause);
    }
}
